import { NextRequest, NextResponse } from 'next/server'
import { handleCors, requireAuth, json } from '@/lib/api-helpers'

// ============ POST - Generate PostgreSQL DDL from Prisma schema ============
export async function POST(request: NextRequest) {
  const corsResp = handleCors(request)
  if (corsResp) return corsResp

  const authUser = requireAuth(request)
  if (authUser instanceof Response) return authUser

  try {
    const body = await request.json()
    const { format } = body // 'ddl' | 'seed' | 'env' | 'schema'

    if (format === 'ddl') {
      return json({ data: { sql: generateDDL(), type: 'ddl' } })
    }
    if (format === 'seed') {
      return json({ data: { sql: generateSeedSQL(), type: 'seed' } })
    }
    if (format === 'env') {
      return json({ data: { sql: generateEnvConfig(body), type: 'env' } })
    }
    if (format === 'schema') {
      return json({ data: { sql: generatePrismaSchema(), type: 'schema' } })
    }

    // Default: return all
    return json({
      data: {
        ddl: generateDDL(),
        seed: generateSeedSQL(),
        env: generateEnvConfig(body),
        schema: generatePrismaSchema(),
      },
    })
  } catch (error) {
    console.error('[Database SQL Generation Error]', error)
    return json({ error: 'Failed to generate SQL.' }, 500)
  }
}

