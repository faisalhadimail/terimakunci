// ============ ENV Config Generation ============
function generateEnvConfig(body: Record<string, unknown>): string {
  const host = (body.host as string) || 'db.xxxxx.supabase.co'
  const port = (body.port as string) || '5432'
  const name = (body.name as string) || 'postgres'
  const user = (body.user as string) || 'postgres.xxxxx'
  const password = (body.password as string) || 'your-password-here'
  const pool = (body.pool as string) || '10'

  const connectionString = `postgresql://${user}:${password}@${host}:${port}/${name}?pgbouncer=true&connect_timeout=15`

  const lines: string[] = []
  lines.push(`# ============================================`)
  lines.push(`# PropNusa Environment Variables`)
  lines.push(`# Database: Supabase (PostgreSQL)`)
  lines.push(`# ============================================`)
  lines.push(``)
  lines.push(`# Database Connection (Supabase)`)
  lines.push(`DATABASE_URL="${connectionString}"`)
  lines.push(``)
  lines.push(`# Direct connection (untuk migration)`)
  lines.push(`DIRECT_URL="postgresql://${user}:${password}@${host}:${port}/${name}?connect_timeout=15"`)
  lines.push(``)
  lines.push(`# Connection Pool (Supabase Pooler)`)
  lines.push(`DATABASE_URL_POOL="${connectionString}"`)
  lines.push(``)
  lines.push(`# App Config`)
  lines.push(`NEXT_PUBLIC_APP_URL="https://www.propnusa.com"`)
  lines.push(`NEXTAUTH_SECRET="your-secret-key-min-32-characters"`)
  lines.push(`NEXTAUTH_URL="https://www.propnusa.com"`)
  lines.push(``)
  lines.push(`# Connection Pool Size`)
  lines.push(`DATABASE_POOL_SIZE=${pool}`)

  return lines.join('\n')
}

